    public void TampilData() {
        DftTblModel_buku = new DefaultTableModel();
        DftTblModel_buku.addColumn("ID");
        DftTblModel_buku.addColumn("Judul Buku");
        DftTblModel_buku.addColumn("Tanggal Pinjam");
        DftTblModel_buku.addColumn("Tanggal Harus Kembali");
        DftTblModel_buku.addColumn("Tanggal Kembali");
        DftTblModel_buku.addColumn("Denda");
        DftTblModel_buku.addColumn("Biaya Sewa");
        tabelbuku.setModel(DftTblModel_buku);
        Connection conn = konekdb.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from sewabuku";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_buku.addRow(new Object[]{
                    res.getString("id"),
                    res.getString("judul"),
                    res.getString("tanggal_pinjam"),
                    res.getString("tanggal_harus_kembali"),
                    res.getString("tanggal_kembali"),
                    res.getString("denda"),
                    res.getString("biaya_sewa"),
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
    public void Tampil_Jam(){
        ActionListener taskPerformer = new ActionListener() {
 
        @Override
            public void actionPerformed(ActionEvent evt) {
            String nol_jam = "", nol_menit = "",nol_detik = "";
 
            java.util.Date dateTime = new java.util.Date();
            int nilai_jam = dateTime.getHours();
            int nilai_menit = dateTime.getMinutes();
            int nilai_detik = dateTime.getSeconds();
 
            if(nilai_jam <= 9) nol_jam= "0";
            if(nilai_menit <= 9) nol_menit= "0";
            if(nilai_detik <= 9) nol_detik= "0";
 
            String jam = nol_jam + Integer.toString(nilai_jam);
            String menit = nol_menit + Integer.toString(nilai_menit);
            String detik = nol_detik + Integer.toString(nilai_detik);
 
            jam_sekarang.setText(jam+":"+menit+":"+detik+"");
            }
        };
    new Timer(1000, taskPerformer).start();
    } 
    
