    private void TanggalKembali() {
	SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dddd");
        int tambah = 7;
        Date tgl = new java.util.Date();
        Calendar calTambah = Calendar.getInstance();

        calTambah.setTime(tgl);
        calTambah.add(Calendar.DAY_OF_MONTH, tambah);

        tanggal_kembali.setText(sdf.format(calTambah.getTime()));

    }
    
    public String GetTanggalHarusKembali(int id){
        String tanggal_harusKembali = "";
        try{
            Connection conn = konekdb.getConnection();
            Statement st = conn.createStatement();
            String querry = "SELECT tanggal_harus_kembali FROM sewabuku WHERE id = "+id;
            System.out.println(querry);
            ResultSet rs = st.executeQuery(querry);
            while (rs.next()) {
                tanggal_harusKembali = rs.getString("tanggal_harus_kembali");
            }   
        }  
        catch(SQLException e){
            System.out.println("Error");
        }
        return tanggal_harusKembali;
    }
    
    
